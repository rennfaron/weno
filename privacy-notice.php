<p><strong>How Weno Protects and Secures a User&#8217;s Personal Information</strong><br />
Weno works hard to ensure that each user&#8217;s personal information remains private and secure. Our security measures include computer safeguards, secured files, and employee security training. Weno adopts and enforces other security measures that comply with federal law which are included in our policies and procedures covering administrative, physical and technical safeguards.</p>
<p>Weno only stores personal user data in the United States of America to allow U.S. law enforcement agencies to prosecute if data is stolen or breached.</p>
<p>Weno automatically encrypts your personal information in transit using the Secure Sockets Layer protocol (SSL) with an encryption key length of 256-bit AES SSL. Weno also utilizes a 2048-bit public key for high security standards.</p>
<p>Information based entries such as user passwords and credit card information are encrypted on Weno&#8217;s server(s). User information is stored on computers with limited accesses that are located in controlled facilities.</p>
<p>Messages sent to and from user&#8217;s Weno mail account are only accessed by their intended recipients via their secure log in. Each message has a unique ID assigned to it. Each message is date and time stamped when delivered.</p>
<p>All Weno&#8217;s registered users are unique with their own account authorizing credentials. Each user log in requires a complex password (8-20 case sensitive, alpha-numeric or symbolic characters). Weno will automatically lock access after a few consecutively failed access attempts. This lock out requires the users to reset their password. Weno also protects users with an automatic log off after a pre- determined time of continuous inactivity. The automatic lock out and automatic log off specific settings are determined by Weno.</p>
<p>Firewalls and anti-virus protections are installed on Weno&#8217;s server(s) and the necessary software updates and patches for these are maintained by a team of qualified professionals; however Weno highly recommends that all users install and utilize their own anti-virus and firewall solutions.</p>
<p>If a user navigates to any third party link from Weno, the user will be leaving the Weno site and entering the third party&#8217;s web page. Protection of the user&#8217;s privacy is governed by the 3 rd parties privacy policies alone.</p>
